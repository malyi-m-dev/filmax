package com.filmax.core.ui.components

import com.filmax.core.domain.error.AppError

/** Заголовок и пояснение ошибки — то, что читает пользователь. */
data class AppErrorText(val title: String, val message: String)

/**
 * Единственное место, где [AppError] превращается в человеческие слова: мобильная модалка
 * [FilmaxErrorModal] и карточка ошибки TV-плеера берут тексты отсюда, а различаются только
 * оформлением (иконки, цвета и кнопки — забота модалки). Держать два набора формулировок
 * значило бы рано или поздно развести их между форм-факторами.
 */
fun appErrorText(error: AppError): AppErrorText = when (error) {
    AppError.Offline -> AppErrorText(
        "Нет подключения",
        "Проверьте интернет-соединение и попробуйте снова.",
    )

    AppError.Server -> AppErrorText(
        "Что-то пошло не так",
        "На нашей стороне сбой. Мы уже разбираемся — попробуйте через минуту.",
    )

    AppError.Timeout -> AppErrorText(
        "Сервер долго отвечает",
        "Не дождались ответа сервера. Проверьте соединение и повторите запрос.",
    )

    AppError.NotFound -> AppErrorText(
        "Контент недоступен",
        "Похоже, этот тайтл больше не в каталоге или был перемещён.",
    )

    AppError.Empty -> AppErrorText(
        "Ничего не найдено",
        "По вашему запросу нет результатов. Измените формулировку или сбросьте фильтры.",
    )

    AppError.Premium -> AppErrorText(
        "Только для Premium",
        "Оформите подписку Filmax Premium, чтобы смотреть в 4K HDR без рекламы.",
    )

    AppError.Region -> AppErrorText(
        "Недоступно в регионе",
        "Этот контент недоступен в вашей стране из-за лицензионных ограничений.",
    )

    AppError.Auth -> AppErrorText(
        "Сессия истекла",
        "Время сессии вышло. Войдите снова, чтобы продолжить просмотр.",
    )

    AppError.Playback -> AppErrorText(
        "Ошибка воспроизведения",
        "Не удалось загрузить видео. Попробуйте снизить качество или повторить.",
    )
}
